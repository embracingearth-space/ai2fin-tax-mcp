/**
 * Bundle the Tax MCP Worker into a single dependency-free .js module.
 * embracingearth.space
 *
 * Inlines data/rates.json and transpiles src/index.ts with the TypeScript
 * compiler (ts.transpileModule) into one ES module — so dist/worker.js can be
 * pasted straight into the Cloudflare dashboard editor (no build, no wrangler)
 * for environments where the wrangler CLI can't run (e.g. TLS-intercepted npm).
 * Using the real compiler (instead of regex stripping) means the bundler is
 * robust to any TypeScript the Worker grows. The canonical deploy is still
 * `wrangler deploy` from src/index.ts; this is the fallback artifact.
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const ts = require('typescript');

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const src = readFileSync(join(root, 'src', 'index.ts'), 'utf8');
const rates = readFileSync(join(root, 'data', 'rates.json'), 'utf8').trim();

// Inline the rates dataset in place of the JSON import — transpileModule does no
// module resolution, so the import must become a literal before stripping types.
const importRe = /^import rates from '\.\.\/data\/rates\.json';$/m;
if (!importRe.test(src)) {
  console.error("bundle: couldn't find the rates JSON import to inline — has src/index.ts changed?");
  process.exit(1);
}
const withRates = src.replace(importRe, `const rates = ${rates};`);

// Inline the landing module the same way — transpileModule does no module
// resolution, so its import must become a local const before types are stripped.
const landingImportRe = /^import \{ LANDING_HTML \} from '\.\/landing';$/m;
if (!landingImportRe.test(withRates)) {
  console.error("bundle: couldn't find the landing import to inline — has src/index.ts changed?");
  process.exit(1);
}
const landingSrc = readFileSync(join(root, 'src', 'landing.ts'), 'utf8').replace(/^export const LANDING_HTML/m, 'const LANDING_HTML');
const withAll = withRates.replace(landingImportRe, landingSrc);

// Strip ALL TypeScript with the real compiler — no fragile per-annotation regex.
const { outputText, diagnostics } = ts.transpileModule(withAll, {
  reportDiagnostics: true,
  compilerOptions: {
    target: ts.ScriptTarget.ES2022,
    module: ts.ModuleKind.ESNext, // keep `export default {…}` for the Workers ES-module format
    removeComments: false,
  },
});

// transpileModule can still emit JS when it reports errors — fail fast on any
// diagnostic rather than shipping a quietly-broken bundle.
if (diagnostics && diagnostics.length) {
  const msgs = diagnostics.map((d) => ts.flattenDiagnosticMessageText(d.messageText, '\n'));
  console.error('bundle: transpile reported diagnostics — aborting:\n  ' + msgs.join('\n  '));
  process.exit(1);
}

// Sanity: the JSON import is gone, the data is inlined, and no TS type syntax slipped through.
if (outputText.includes('import rates from') || !outputText.includes('const rates =')) {
  console.error('bundle: rates were not inlined correctly.');
  process.exit(1);
}
if (outputText.includes("from './landing'") || !outputText.includes('const LANDING_HTML')) {
  console.error('bundle: landing module was not inlined correctly.');
  process.exit(1);
}
if (/\binterface \w|\: Promise<|\bas Row\b/.test(outputText)) {
  console.error('bundle: TypeScript syntax survived transpilation — aborting.');
  process.exit(1);
}
// A single-file Worker bundle must have NO imports — the only one (rates JSON) was
// inlined above. Any surviving static or dynamic import means an un-inlined
// dependency that would break the pasted-into-dashboard artifact.
const strayImport = outputText.split('\n').find((l) => /^\s*import[\s({'"]/.test(l) || /[^.\w]import\s*\(/.test(l));
if (strayImport) {
  console.error('bundle: unexpected import survived bundling — not a self-contained Worker:\n  ' + strayImport.trim());
  process.exit(1);
}

const banner = '// AI2Fin public Tax MCP — single-file Cloudflare Worker bundle.\n' +
  '// GENERATED by scripts/bundle-worker.mjs from src/index.ts — do not edit by hand.\n' +
  '// Paste into the Cloudflare dashboard Worker editor and Deploy, or `wrangler deploy`.\n\n';

mkdirSync(join(root, 'dist'), { recursive: true });
writeFileSync(join(root, 'dist', 'worker.js'), banner + outputText, 'utf8');
console.log(`Wrote dist/worker.js (${(banner + outputText).length} bytes, ${JSON.parse(rates).length} rate rows).`);
